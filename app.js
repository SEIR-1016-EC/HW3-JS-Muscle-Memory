function evenCaps(sentence) {
  let newSentence = "";
  for (let i = 0; i < sentence.length; i++) {
    let char = sentence[i];
    if (i % 2 === 0) {
      let capitalChar = char.toUpperCase();
      newSentence += capitalChar;
    } else {
      newSentence += char;
    }
  }
  return newSentence;
}

